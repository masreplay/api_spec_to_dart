import 'exports.dart';
part 'currency_model.freezed.dart';
part 'currency_model.g.dart'; // CurrencyModel

@freezed
abstract class CurrencyModel with _$CurrencyModel {
  const CurrencyModel._();

  @jsonSerializable
  const factory CurrencyModel({
    /// currency
    @JsonKey(name: CurrencyModel.currencyKey_) Currency? currency,

    /// iso4217
    @JsonKey(name: CurrencyModel.iso4217Key_) Iso4217? iso4217,
  }) = _CurrencyModel;

  factory CurrencyModel.fromJson(Map<String, dynamic> json) =>
      _$CurrencyModelFromJson(json);

  static const String currencyKey_ = r'currency';

  static const String iso4217Key_ = r'iso4217';
}
